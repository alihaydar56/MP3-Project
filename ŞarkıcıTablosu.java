public void Arama(String kelime){
    TableRowSorter<DefaultTableModel> search1=new TableRowSorter<DefaultTableModel>(model);
    TableRowSorter<DefaultTableModel> search2=new TableRowSorter<DefaultTableModel>(model2);
    
    tablo.setRowSorter(search1);
    tablo2.setRowSorter(search2);
    
    search1.setRowFilter(RowFilter.regexFilter(kelime));
    search2.setRowFilter(RowFilter.regexFilter(kelime));
    
    
}
