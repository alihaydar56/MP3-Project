    public void VerileriAll(){
        model2.setRowCount(0);
        ArrayList<Muzikler> muzik=new ArrayList<Muzikler>();
        muzik=işlemler.FavAl();
        if(muzik != null) {            
            for(Muzikler m: muzik){
                Object[] ekle={m.getId(),m.getŞarkı_ismi()};
                
                model2.addRow(ekle);   
            }
        }
    }
    public void VerileriAl(){
        model.setRowCount(0);
        i=0;
        ArrayList<Muzikler> muzik=new ArrayList<Muzikler>();
        muzik=işlemler.VeriAl();
        if(muzik != null) {            
            for(Muzikler m: muzik){
                Object[] ekle={m.getId(),m.getŞarkı_ismi()};
                
                model.addRow(ekle);
                 i++;       
            }
        }
        şarkı_sayısı.setText(String.valueOf(i));
    }

