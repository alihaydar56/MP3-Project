    private void silme_butonuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_silme_butonuActionPerformed
       int seçili_satır=tablo.getSelectedRow();
       
       if(seçili_satır ==-1){
           if(model.getRowCount()==0){
               JOptionPane.showMessageDialog(this, "Listeniz zaten boş");
           }
           else{
               JOptionPane.showMessageDialog(this, "Lütfen silinecek satırı seçiniz");
           }
       }
       else{
           int id=(int) model.getValueAt(seçili_satır, 0);
           işlemler.VeriSil(id);
           VerileriAl();
       }
    }//GEN-LAST:event_silme_butonuActionPerformed

    private void favori_butonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_favori_butonActionPerformed
    
    model2=(DefaultTableModel) tablo2.getModel();
    int seçili_satır=tablo.getSelectedRow();
    if(seçili_satır==-1){
        if(tablo.getRowCount()==0){
            JOptionPane.showMessageDialog(this,"Tablonuz boş!!!");
        }
        else{
            JOptionPane.showMessageDialog(this,"Lütfen Favori listenize eklemek istediginiz şarkıyı seçiniz!");
        }
    }
    else{
        int id=(int) model.getValueAt(seçili_satır,0);
        String şarkı=(String) model.getValueAt(seçili_satır,1);
        Object[] ekle={id,şarkı};
        model2.addRow(ekle);
        işlemler.favekle(şarkı);
        
        VerileriAll();
        
        
        
    }
    }//GEN-LAST:event_favori_butonActionPerformed

